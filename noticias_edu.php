						<h5 class="text">
						<p>&nbsp;</p>
						En cuatro días de conferencias, ponencias y presentaciones en el Mobile World Congres 2016 sobre la industria móvil no ha faltado la aplicación de la tecnología móvil a la educación. mSchools, un proyecto que tiene como objetivo ayudar a alumnado y docentes a integrar tecnologías digitales en el aula de manera eficaz, ha organizado en el último día del MWC el seminario "Changing Education Together".
						<p>&nbsp;</p>
						En esta segunda edición se ha hablado sobre el uso responsable de los dispositivos móviles en la educación, un tema fundamental para mShools. En una entrevista con el responsable del proyecto mSchools, Albert Forn, explicaba que "los dispositivos móviles tienen la capacidad de transformar la enseñanza, porque habilitarán la educación adaptativa, la transversalidad de la tecnología en cualquier materia, la adecuación de los contenidos a las profesiones de futuro, la mejor formación del profesorado, la transformación del aula o la mayor implicación del alumno".
						<p>&nbsp;</p>
						En las diversas ponencias de los principales expertos en mEducation, han abordado desde la importancia del uso de la tecnología móvil para alumnado y docente. Brian Subirana, experto en Digital Learning del Massachusets Institute of Technology (MIT) ha hablado de la necesidad enseñar al alumnado a comunicar y compartir su aprendizaje, la necesidad de cambiar la medida de valor y puntuación de los centros educativos y la unificación del software utilizado en las escuelas.
						<p>&nbsp;</p>
						Posteriormente, el panel de expertos ha debatido sobre la importancia de la formación del profesorado, reconociendo que el 30% de la inversión destinada a impulsar las TIC debe dirigirse a la formación de los docentes. En este sentido, Albert Forn afirma que "por supuesto, la formación al profesorado es imprescindible y una inversión con retorno asegurado […] La evolución de las TIC es un reto para todos. Vale la pena no olvidar el tremendo cambio social que estamos viviendo, en el que el papel del maestro está cambiando de forma radical. Las TIC son una pata importante, pero no son la única, el profesorado necesita que el sistema educativo cambie con los tiempos y le permita desarrollar su labor."
